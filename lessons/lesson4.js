//Relational or comparison operators

// > greater than
// < less than
// >= greater than equal to
// <= less than equal to

let result = 5 >= 5
console.log(result);

//Equality operators
let x = 1
console.log(x == '1'); //loose comparison
//True because the value is the same, data type does not matter

console.log(x === '1'); //strict comparison
//False because even though the value is the same, the data type is not the same